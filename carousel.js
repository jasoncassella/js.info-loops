for (let i = 1; i <= 10; i++) {
	if (i === 1) {
		console.log('carousel has gone 1 turn');
	} else {
		console.log(`carousel has gone ${i} turns`);
	}
}

let turn = 1;
while (turn <= 10) {
	if (turn === 1) {
		console.log('carousel has gone 1 turn');
	} else {
		console.log(`carousel has gone ${turn} turns`);
	}
	turn++;
}
